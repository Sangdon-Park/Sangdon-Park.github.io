def find_default_input() -> Path:
    data_dir = next(path.parent for path in Path("data").rglob("00-preview.pptx"))
    return next(data_dir.glob("03-*.pptx"))


def build_backup_path(src: Path, backup_dir: Path) -> Path:
    timestamp = datetime.now().strftime("%Y%m%d-%H%M%S")
    return backup_dir / f"{src.stem}.backup-{timestamp}{src.suffix}"


def set_slide_note(slide, text: str) -> None:
    for shape in slide.notes_slide.shapes:
        if not getattr(shape, "is_placeholder", False):
            continue
        if shape.placeholder_format.type != PP_PLACEHOLDER.BODY:
            continue
        shape.text_frame.text = text
        return
    raise RuntimeError("BODY placeholder not found on notes page")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--input", type=Path, default=None)
    parser.add_argument(
        "--backup-dir",
        type=Path,
        default=Path(tempfile.gettempdir()) / "codex-pptx-backups",
    )
    args = parser.parse_args()

    src = args.input or find_default_input()
    args.backup_dir.mkdir(parents=True, exist_ok=True)
    backup_path = build_backup_path(src, args.backup_dir)
    shutil.copy2(src, backup_path)

    prs = Presentation(src)
    expected_slides = len(prs.slides)
    if len(NOTE_TEXTS) != expected_slides + 1:
        raise ValueError(
            f"Expected {expected_slides} notes, found {len(NOTE_TEXTS) - 1}"
        )

    for slide_number, slide in enumerate(prs.slides, start=1):
        set_slide_note(slide, NOTE_TEXTS[slide_number])

    prs.save(src)
    print(f"UPDATED {src}")
    print(f"BACKUP {backup_path}")
    print(f"SLIDES {expected_slides}")


if __name__ == "__main__":
    main()
